#!/usr/bin/env node
/**
 * generate-package-stubs.js — Materialize packages/* from scope-manifest.js.
 *
 * Each @zero-server/* package is now a TRUE STANDALONE bundle.  Instead of a
 * thin re-export of @zero-server/sdk, the package ships its own copy of the
 * relevant lib/ source files.  Cross-scope require() calls are satisfied
 * either by small shim files or by listing the peer package as a dependency.
 *
 * Generated per package:
 *   packages/<name>/lib/             copied source (with require rewrites applied)
 *   packages/<name>/lib/debug.js     (only when bundleDebug: true)
 *   packages/<name>/lib/<shim>       extra shim files (from extraShims)
 *   packages/<name>/index.js         CJS entry — requires from ./lib/, not the SDK
 *   packages/<name>/index.d.ts       TypeScript re-exports from bundled ./types/<scope>.d.ts
 *   packages/<name>/package.json     proper deps (no SDK runtime dep)
 *   packages/<name>/LICENSE          copied from root
 *
 * Usage:  npm run packages:generate
 */
'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const PKG  = JSON.parse(fs.readFileSync(path.join(ROOT, 'package.json'), 'utf8'));
const sdkVersion = PKG.version;
const sdkName    = PKG.name;           // '@zero-server/sdk'
const { scopes } = require('./scope-manifest');

// Verify every exported name actually exists in the SDK at generation time.
const sdk = require(path.join(ROOT, 'index.js'));
const sdkValueNames = new Set(Object.keys(sdk));

const BANNER = '// AUTO-GENERATED by .tools/generate-package-stubs.js — edit .tools/scope-manifest.js and re-run `npm run packages:generate`.\n';
const PACKAGES_DIR = path.join(ROOT, 'packages');
fs.mkdirSync(PACKAGES_DIR, { recursive: true });

const licensePath   = path.join(ROOT, 'LICENSE');
const licenseSource = fs.existsSync(licensePath) ? fs.readFileSync(licensePath) : null;

// ─────────────────────────────────────────────────────────────────────────────
//  Helpers
// ─────────────────────────────────────────────────────────────────────────────

/** Apply all require-string replacements to a file's content. */
function applyRewrites(content, rewrites) {
    for (const [from, to] of Object.entries(rewrites)) {
        // Replace all occurrences (global string replace via split/join)
        while (content.includes(from)) {
            content = content.split(from).join(to);
        }
    }
    return content;
}

/** Copy a single file, applying require rewrites to its text content. */
function copyFileWithRewrites(src, dst, rewrites) {
    fs.mkdirSync(path.dirname(dst), { recursive: true });
    let content = fs.readFileSync(src, 'utf8');
    if (Object.keys(rewrites).length > 0) {
        content = applyRewrites(content, rewrites);
    }
    fs.writeFileSync(dst, content);
}

/**
 * Recursively copy a directory, applying require rewrites to every .js file.
 * Non-.js files (data files, etc.) are copied verbatim.
 */
function copyDirRecursive(src, dst, rewrites) {
    fs.mkdirSync(dst, { recursive: true });
    for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
        const srcEntry = path.join(src, entry.name);
        const dstEntry = path.join(dst, entry.name);
        if (entry.isDirectory()) {
            copyDirRecursive(srcEntry, dstEntry, rewrites);
        } else if (entry.name.endsWith('.js')) {
            copyFileWithRewrites(srcEntry, dstEntry, rewrites);
        } else {
            fs.copyFileSync(srcEntry, dstEntry);
        }
    }
}

/** Wipe and recreate the lib/ subdirectory inside a package folder. */
function cleanPackageLib(pkgDir) {
    const libDir = path.join(pkgDir, 'lib');
    if (fs.existsSync(libDir)) {
        fs.rmSync(libDir, { recursive: true, force: true });
    }
    fs.mkdirSync(libDir, { recursive: true });
}

// ─────────────────────────────────────────────────────────────────────────────
//  index.js builder for single-entry scopes
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Build the CJS index.js for a scope that has a single require() entry point.
 * Uses scope.localMap to rename locals to public API names.
 */
function buildSingleEntryIndexJs(scope, entryPath) {
    const localMap = scope.localMap || {};
    const lines = scope.exports.map((pubName) => {
        const localName = localMap[pubName] || pubName;
        return `    ${pubName}: lib.${localName},`;
    });
    return [
        BANNER.trimEnd(),
        "'use strict';",
        `const lib = require(${JSON.stringify(entryPath)});`,
        '',
        'module.exports = {',
        ...lines,
        '};',
        '',
    ].join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
//  package.json builder
// ─────────────────────────────────────────────────────────────────────────────

function buildPackageJson(scope) {
    const extraDeps = {};
    for (const [depName, val] of Object.entries(scope.pkgDependencies || {})) {
        extraDeps[depName] = val === true ? sdkVersion : val;
    }

    const pkg = {
        name:        `@zero-server/${scope.name}`,
        version:     sdkVersion,
        description: scope.summary,
        keywords:    ['zero-server', 'zero-server', scope.name],
        author:      PKG.author,
        license:     PKG.license,
        main:        './index.js',
        types:       './index.d.ts',
        exports: {
            '.': {
                types:   './index.d.ts',
                require: './index.js',
                default: './index.js',
            },
            './package.json': './package.json',
        },
        files: ['lib', 'types', 'index.js', 'index.d.ts', 'README.md', 'LICENSE'],
        repository: {
            type:      'git',
            url:       'git+https://github.com/tonywied17/zero-server.git',
            directory: `packages/${scope.name}`,
        },
        bugs:     { url: 'https://github.com/tonywied17/zero-server/issues' },
        homepage: `https://github.com/tonywied17/zero-server/tree/main/packages/${scope.name}#readme`,
        engines:  { node: '>=18.0.0' },
        publishConfig: {
            registry: 'https://registry.npmjs.org/',
            access:   'public',
        },
        sideEffects: false,
    };

    if (Object.keys(extraDeps).length > 0) {
        pkg.dependencies = extraDeps;
    }

    // @zero-server/sdk is OPTIONAL — only needed by TypeScript users for type
    // declarations (index.d.ts re-exports from bundled types/).
    pkg.peerDependencies = { [sdkName]: `>=${sdkVersion}` };
    pkg.peerDependenciesMeta = { [sdkName]: { optional: true } };

    return pkg;
}

// ─────────────────────────────────────────────────────────────────────────────
//  TypeScript declaration builder (unchanged — re-exports from SDK types)
// ─────────────────────────────────────────────────────────────────────────────

// ─────────────────────────────────────────────────────────────────────────────
//  TypeScript declaration builder — emits export * from local ./types/<file>
// ─────────────────────────────────────────────────────────────────────────────

function buildDts(scope) {
    const files = scope.typesFiles;
    if (!files || files.length === 0) {
        return BANNER.trimEnd() + '\nexport {};\n';
    }
    const lines = [BANNER.trimEnd()];
    for (const tf of files) {
        lines.push(`export * from './types/${tf}';`);
    }
    lines.push('');
    return lines.join('\n');
}

// ─────────────────────────────────────────────────────────────────────────────
//  Main loop
// ─────────────────────────────────────────────────────────────────────────────

let total   = 0;
const missing = [];

for (const scope of scopes) {
    if (!scope.exports || scope.exports.length === 0) {
        throw new Error(`Scope "${scope.name}" lists no exports.`);
    }

    // Validate that every exported name exists in the SDK
    for (const name of scope.exports) {
        if (!sdkValueNames.has(name)) {
            missing.push({ scope: scope.name, name });
        }
    }

    const dir = path.join(PACKAGES_DIR, scope.name);
    fs.mkdirSync(dir, { recursive: true });

    // ── Clean the lib/ directory so stale files don't linger ──────────────
    cleanPackageLib(dir);
    const libDir = path.join(dir, 'lib');

    const rewrites = scope.requireRewrites || {};

    // ── 1. Copy source directories ─────────────────────────────────────────
    if (scope.sourceDirs) {
        for (const srcDirName of scope.sourceDirs) {
            const src = path.join(ROOT, 'lib', srcDirName);
            const dst = path.join(libDir, srcDirName);
            copyDirRecursive(src, dst, rewrites);
        }
    }
    if (scope.sourceDir) {
        const src = path.join(ROOT, 'lib', scope.sourceDir);
        const dst = path.join(libDir, scope.sourceDir);
        copyDirRecursive(src, dst, rewrites);
    }

    // ── 2. Copy individual source files ────────────────────────────────────
    if (scope.sourceFiles) {
        for (const srcFile of scope.sourceFiles) {
            const src = path.join(ROOT, 'lib', srcFile);
            const dst = path.join(libDir, srcFile);
            copyFileWithRewrites(src, dst, rewrites);
        }
    }
    if (scope.sourceFile) {
        const src = path.join(ROOT, 'lib', scope.sourceFile);
        const dst = path.join(libDir, scope.sourceFile);
        copyFileWithRewrites(src, dst, rewrites);
    }

    // ── 3. Bundle debug.js ─────────────────────────────────────────────────
    if (scope.bundleDebug) {
        fs.copyFileSync(path.join(ROOT, 'lib', 'debug.js'), path.join(libDir, 'debug.js'));
    }

    // ── 4. Extra shim files ────────────────────────────────────────────────
    if (scope.extraShims) {
        for (const shim of scope.extraShims) {
            const shimDst = path.join(libDir, shim.file);
            fs.mkdirSync(path.dirname(shimDst), { recursive: true });
            fs.writeFileSync(shimDst, shim.content);
        }
    }

    // ── 5. index.js ───────────────────────────────────────────────────────
    let indexJs;
    if (scope.indexJs) {
        // Explicit — prepend banner
        indexJs = BANNER + scope.indexJs;
    } else {
        // Auto-generate from a single entry module
        let entryPath;
        if (scope.sourceDir) {
            entryPath = `./lib/${scope.sourceDir}`;
        } else if (scope.sourceFile) {
            entryPath = `./lib/${scope.sourceFile.replace(/\.js$/, '')}`;
        } else {
            throw new Error(
                `Scope "${scope.name}" has no sourceDir/sourceFile and no indexJs override.`
            );
        }
        indexJs = buildSingleEntryIndexJs(scope, entryPath);
    }
    fs.writeFileSync(path.join(dir, 'index.js'), indexJs);

    // ── 6. package.json ───────────────────────────────────────────────────
    fs.writeFileSync(
        path.join(dir, 'package.json'),
        JSON.stringify(buildPackageJson(scope), null, 2) + '\n'
    );

    // ── 7. Copy types/*.d.ts ───────────────────────────────────────────────
    const typesDir = path.join(dir, 'types');
    fs.mkdirSync(typesDir, { recursive: true });
    const srcTypesDir = path.join(ROOT, 'types');
    for (const entry of fs.readdirSync(srcTypesDir)) {
        if (entry.endsWith('.d.ts')) {
            fs.copyFileSync(path.join(srcTypesDir, entry), path.join(typesDir, entry));
        }
    }

    // ── 8. index.d.ts ─────────────────────────────────────────────────────
    fs.writeFileSync(path.join(dir, 'index.d.ts'), buildDts(scope));

    // ── 9. LICENSE ────────────────────────────────────────────────────────
    if (licenseSource) {
        fs.writeFileSync(path.join(dir, 'LICENSE'), licenseSource);
    }

    total++;
    console.log(`  ✓  @zero-server/${scope.name}`);
}

// ─────────────────────────────────────────────────────────────────────────────
//  Report
// ─────────────────────────────────────────────────────────────────────────────

if (missing.length > 0) {
    console.error('\n⚠  Names listed in scope-manifest but NOT exported by the SDK:');
    for (const { scope: s, name: n } of missing) {
        console.error(`   @zero-server/${s}: ${n}`);
    }
    console.error('   Fix scope-manifest.js or index.js and re-run.\n');
    process.exitCode = 1;
} else {
    console.log(`\n✓  Generated ${total} standalone package bundles.\n`);
}
