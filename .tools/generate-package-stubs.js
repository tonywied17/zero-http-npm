#!/usr/bin/env node
/**
 * generate-package-stubs.js — Materialize packages/* from scope-manifest.js.
 *
 * Each scoped @zero-server/* package is a thin re-export of the
 * corresponding surface from @zero-server/sdk. We generate:
 *
 *   packages/<name>/package.json   narrow metadata + same version as the SDK
 *   packages/<name>/index.js       CJS re-export of just this scope's surface
 *   packages/<name>/index.d.ts     TS re-export of just this scope's surface
 *
 * The scope is verified by `require('@zero-server/sdk')` at runtime — if a
 * name in the manifest is not actually exported, we abort so the manifest
 * stays in lock-step with reality.
 *
 * Per-package READMEs are produced separately by generate-scope-docs.js so
 * the API.md / docs site build can run independently.
 *
 * Usage:  npm run packages:generate
 */
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const PKG = JSON.parse(fs.readFileSync(path.join(ROOT, 'package.json'), 'utf8'));
const sdkVersion = PKG.version;
const sdkName = PKG.name;
const { scopes } = require('./scope-manifest');

// Load the SDK so we can verify every name exists at runtime.
const sdk = require(path.join(ROOT, 'index.js'));
const sdkValueNames = new Set(Object.keys(sdk));

const banner = '// AUTO-GENERATED by .tools/generate-package-stubs.js — edit .tools/scope-manifest.js and re-run `npm run packages:generate`.\n';

const PACKAGES_DIR = path.join(ROOT, 'packages');
fs.mkdirSync(PACKAGES_DIR, { recursive: true });

const licensePath = path.join(ROOT, 'LICENSE');
const licenseSource = fs.existsSync(licensePath) ? fs.readFileSync(licensePath) : null;

let total = 0;
const missing = [];

for (const scope of scopes) {
    if (!scope.exports || scope.exports.length === 0) {
        throw new Error(`Scope "${scope.name}" lists no exports.`);
    }

    const valueNames = [];
    for (const name of scope.exports) {
        if (sdkValueNames.has(name)) {
            valueNames.push(name);
        } else {
            missing.push({ scope: scope.name, name });
        }
    }

    const dir = path.join(PACKAGES_DIR, scope.name);
    fs.mkdirSync(dir, { recursive: true });

    const pkg = {
        name: `@zero-server/${scope.name}`,
        version: sdkVersion,
        description: scope.summary,
        keywords: ['zero-server', 'zero-http', scope.name],
        author: PKG.author,
        license: PKG.license,
        main: './index.js',
        types: './index.d.ts',
        exports: {
            '.': {
                types: './index.d.ts',
                require: './index.js',
                default: './index.js',
            },
            './package.json': './package.json',
        },
        files: ['index.js', 'index.d.ts', 'README.md', 'LICENSE'],
        repository: {
            type: 'git',
            url: 'git+https://github.com/tonywied17/zero-server.git',
            directory: `packages/${scope.name}`,
        },
        bugs: { url: 'https://github.com/tonywied17/zero-server/issues' },
        homepage: `https://github.com/tonywied17/zero-server/tree/main/packages/${scope.name}#readme`,
        engines: { node: '>=18.0.0' },
        publishConfig: {
            registry: 'https://registry.npmjs.org/',
            access: 'public',
        },
        sideEffects: false,
        dependencies: {
            [sdkName]: sdkVersion,
        },
    };
    fs.writeFileSync(path.join(dir, 'package.json'), JSON.stringify(pkg, null, 2) + '\n');

    // CJS re-export
    const cjs = [
        banner.trimEnd(),
        "'use strict';",
        `const sdk = require(${JSON.stringify(sdkName)});`,
        '',
        'module.exports = {',
        ...valueNames.map((n) => `    ${n}: sdk.${n},`),
        '};',
        '',
    ].join('\n');
    fs.writeFileSync(path.join(dir, 'index.js'), cjs);

    // TypeScript declaration — re-export from the SDK's own types
    const dts = [
        banner.trimEnd(),
        valueNames.length > 0
            ? `export { ${valueNames.join(', ')} } from ${JSON.stringify(sdkName)};`
            : 'export {};',
        '',
    ].join('\n');
    fs.writeFileSync(path.join(dir, 'index.d.ts'), dts);

    if (licenseSource) {
        fs.writeFileSync(path.join(dir, 'LICENSE'), licenseSource);
    }

    total++;
}

console.log(`Generated ${total} scoped package stubs at ${sdkName}@${sdkVersion}.`);

if (missing.length > 0) {
    console.warn(`\nWARNING: ${missing.length} symbol(s) listed in scope-manifest are not actually exported by ${sdkName}:`);
    for (const m of missing) console.warn(`  - ${m.scope}: ${m.name}`);
    process.exitCode = 1;
}
